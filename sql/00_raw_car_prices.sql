CREATE TABLE raw_car_prices(
    year SMALLINT,
    make VARCHAR(50),
    model VARCHAR(100),
    trim VARCHAR(100),
    body VARCHAR(50),
    transmission VARCHAR(50),
    vin VARCHAR(50),
    state VARCHAR(50),
    condition NUMERIC(5,2),
    odometer BIGINT,
    color VARCHAR(50),
    interior VARCHAR(50),
    seller VARCHAR(255),
    mmr NUMERIC(10,2),
    sellingprice NUMERIC(10,2),
    saledate TEXT
);

